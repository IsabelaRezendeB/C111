# TUPLAS

# Guarda seus elementos dentro ()
# Imutáveis, não aceita nenhum tipo de atualização

nomes = ('Goku', 'Vegeta', 'Trunks', 'Gohan')

# Exibição
print(nomes)
print(nomes[0])

# Slicing: primeiro elemento é inclusive e o último exclusive
print(nomes[1:3])

print('Funções pré-prontas do Python')
print('Len: ', len(nomes))
print('Max: ', max(nomes))
print('Min: ', min(nomes))
print('Sorted: ', sorted(nomes))